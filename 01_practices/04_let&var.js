// LET

function value() {
  if (true) {
    let c = 12;
  }
  console.log(c);
}
return value;

//The let declaration declares re-assignable, block-scoped local variables

//var*****

function values() {
  if (true) {
    var a = 15;
  }
  console.log(a);
}
return values();

//The var statement declares function-scoped or globally-scoped variables
